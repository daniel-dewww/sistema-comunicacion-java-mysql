   public void cargarDatosTareas(String idAlumnoCarnet){
            tabla = new DefaultTableModel(null,titulos_columnas);
           
       try {
           
           conexion = conexionBD.getConection();
           String consulta = "select tar_nombre,tar_desc,curso_id,ban_fecha from tb_tarea where alumno_carnet='" + idAlumnoCarnet + "'"; //falta where
           sentencia_preparada = conexion.prepareStatement(consulta);
           resultado = sentencia_preparada.executeQuery();
           while(resultado.next()){
               filas[0] = resultado.getString(1);
               filas[1] = resultado.getString(2);
               filas[2] = resultado.getString(3);
               filas[3] = resultado.getString(4);
              
               tabla.addRow(filas);
               tablaTareas.setModel(tabla);
               
               
           }    
           conexion.close();
       } catch (Exception e) {
           System.out.println(e.getMessage());
       }
   }

