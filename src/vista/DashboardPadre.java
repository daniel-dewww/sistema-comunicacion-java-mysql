    private void matematica(String codigo) {
        try {
             
            conexion = conexionBD.getConection();
            String consulta = "select cal_nota from tb_padre inner join tb_calificacion on tb_padre.alumno_carnet = tb_calificacion.alumno_carnet  where curso_id ='1' and pdr_id='" + codigo + "'";
            sentencia_preparada = conexion.prepareStatement(consulta);
            resultado = sentencia_preparada.executeQuery();
            if (resultado.next()) {
                String r = resultado.getString("cal_nota");
                 lblMatematica1.setText(r);
            }
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
